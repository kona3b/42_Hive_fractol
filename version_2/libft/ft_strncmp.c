#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;
	unsigned char	*c1;
	unsigned char	*c2;

	c1 = (unsigned char *)s1;
	c2 = (unsigned char *)s2;
	i = 0;
	if (n == 0)
		return (0);
	while ((i + 1) < n && c1[i] != '\0' && c2[i] != '\0' && c1[i] == c2[i])
		i++;
	return (c1[i] - c2[i]);
}
